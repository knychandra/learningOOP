class Student:

    def __init__(self, name, age, grade):
        self.name = name
        self.age = age
        self.grade = grade

john = Student('John', 19, 85)
james = Student('James', 21, 95)

# printing john's attributes
print(john.age)
print(john.age)
print(john.grade)

# printing james' attributes

print(james.name)
print(james.age)
print(john.grade)
